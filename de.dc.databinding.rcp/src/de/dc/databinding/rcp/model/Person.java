package de.dc.databinding.rcp.model;

public class Person {
	private String name;
	private int age;
	private String numner;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getNumner() {
		return numner;
	}
	public void setNumner(String numner) {
		this.numner = numner;
	}
}
